package Day1Problems;

public class PrimitiveDataTypes {
	       static boolean a;
	       static byte y;
	       static char c;
	       static short s;
	       static int b;
	       static long l;
	       static float f;
	       static double d;
	       
          public static void main(String[] args) {
               System.out.println("default value of boolean : " + a);
        	   System.out.println("default value of integer : " + b);
        	   System.out.println("default value of Character: " + c);
        	   System.out.println("default value of byte : " + y);
        	   System.out.println("default value of short: " + s);
        	   System.out.println("default value of long : " + l);
        	   System.out.println("default value of float : " + f);
        	   System.out.println("default value of double : " + d);
           }
}
